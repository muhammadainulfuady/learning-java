public class KalkulatorSains {
    public static void main(String[] args) {
        double pi = 3.1415926535;
        float jariJariLingkaran = 7.5F;
        float suhuBadan = 36.6F;
        System.out.println("Jari - jari     : " + jariJariLingkaran);
        System.out.println("Luas lingkaran  : " + pi * jariJariLingkaran * jariJariLingkaran);
        System.out.println("Suhu badan      : " + suhuBadan);
    }
}
