package expressionStatementBlock;

public class Express {
    public static void main(String[] args) {
        /*
         * adapun beberapa hal materi mengenai :
         * 1. expression
         * 2. statement
         * 3. block
         */
        /*
         * kode di bawah ini merupakan expression, ciri dari expression sendiri itu
         * adalah yang bisa menghasilkan sebuah nilai->(8 + 9)
         * -
         * kode di bawah ini juga menerapkan statement, dan ciri dari statement sendiri
         * itu adalah line code yang kita tulis itu berakhir sampai dengan titik koma
         * -
         * 
         */
        int sum = 8 + 9;
        System.out.println(sum);
    }
}
